# Block-level wrappers Word places around whole paragraphs / tables.
_TRANSPARENT_BLOCK_WRAPPERS = frozenset(
    {"ins", "del", "moveFrom", "moveTo", "customXml"}
)


def _iter_effective_body_children(parent: Element):
    """Yield the block-level children of ``parent`` in document order,
    descending transparently through revision-tracking and content-control
    wrappers Word can place around whole paragraphs / tables.

    ``<w:ins>`` / ``<w:del>`` / ``<w:moveFrom>`` / ``<w:moveTo>`` /
    ``<w:customXml>`` hold their blocks directly; a block-level ``<w:sdt>``
    (content control) holds them under ``<w:sdtContent>``. Without this a
    tracked-changes or content-control wrapped paragraph would be silently
    dropped (the old ``else: continue`` in :func:`_iter_body_blocks`).
    """
    for child in parent:
        tag = _local(child.tag)
        if tag in _TRANSPARENT_BLOCK_WRAPPERS:
            yield from _iter_effective_body_children(child)
        elif tag == "sdt":
            content = _first_local(child, "sdtContent")
            if content is not None:
                yield from _iter_effective_body_children(content)
        else:
            yield child


def _iter_body_blocks(
    body: Element,
    *,
    ole_blobs: dict[str, bytes] | None = None,
    chem_detection: bool = False,
):
    """Yield IR blocks from ``w:body`` in document order.

    Consecutive list-item paragraphs are merged into one :class:`List`
    so the IR matches what users see in Word (no per-line ``List``
    wrapper). Tables and standalone math paragraphs are emitted as
    they appear.

    ``ole_blobs`` maps relationship ids to the raw bytes of every OLE
    object in the package — used to resolve ``<w:object>`` MathType /
    Equation 3.0 formulas to their MTEF payload. ``None`` skips OLE
    handling entirely (kept for callers that build a body element
    outside of :func:`parse_docx`).
    """
    if ole_blobs is None:
        ole_blobs = {}
    pending_list: list[ListItem] = []
    pending_list_ordered: bool | None = None

    def flush_list():
        nonlocal pending_list, pending_list_ordered
        if pending_list:
            yield List(
                ordered=bool(pending_list_ordered),
                items=pending_list,
            )
            pending_list = []
            pending_list_ordered = None

    for child in _iter_effective_body_children(body):
        tag = _local(child.tag)
        if tag == "p":
            blocks = _convert_paragraph(
                child, ole_blobs=ole_blobs, chem_detection=chem_detection
            )
            for blk in blocks:
                if isinstance(blk, ListItem):
                    ordered = blk.id == "__ordered__"
                    blk.id = None  # carrier flag, not a real id
                    if pending_list_ordered is None:
                        pending_list_ordered = ordered
                    elif pending_list_ordered != ordered:
                        # Flavor changed (ordered ↔ unordered) → split.
                        yield from flush_list()
                        pending_list_ordered = ordered
                    pending_list.append(blk)
                else:
                    yield from flush_list()
                    yield blk
        elif tag == "tbl":
            yield from flush_list()
            yield _convert_table(
                child, ole_blobs=ole_blobs, chem_detection=chem_detection
            )
        elif tag == "oMathPara":
            yield from flush_list()
            yield _math_block_from_omath_para(child)
        else:
            # sectPr and unknown elements are ignored — they don't
            # carry braille-relevant content.
            continue
    yield from flush_list()


