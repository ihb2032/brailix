def _convert_table(
    tbl: Element,
    *,
    ole_blobs: dict[str, bytes] | None = None,
    chem_detection: bool = False,
) -> Table:
    rows: list[TableRow] = []
    # Descend transparently through revision-tracking / content-control
    # wrappers, the same way the body walker does — a row or cell wrapped in
    # <w:ins> (inserted) or <w:sdt> (content control) is common in real
    # "accept-changes-first" documents and would otherwise be silently
    # dropped, leaving the braille table missing whole rows / cells.
    for tr in _iter_effective_body_children(tbl):
        if _local(tr.tag) != "tr":
            continue
        cells: list[TableCell] = []
        for tc in _iter_effective_body_children(tr):
            if _local(tc.tag) != "tc":
                continue
            cells.append(
                _convert_table_cell(
                    tc, ole_blobs=ole_blobs, chem_detection=chem_detection
                )
            )
        if cells:
            rows.append(TableRow(cells=cells))
    return Table(rows=rows)


def _convert_table_cell(
    tc: Element,
    *,
    ole_blobs: dict[str, bytes] | None = None,
    chem_detection: bool = False,
) -> TableCell:
    """Flatten a cell's paragraphs into one TableCell text.

    The braille table renderer expects flat cell text (no nested
    paragraphs); we join paragraph contents with a newline so a
    multi-paragraph cell still preserves the visual break. A nested
    table inside the cell is flattened the same way — its inner cells'
    text is folded in rather than dropped.
    """
    parts: list[str] = []
    # Same transparent descent as _convert_table: a paragraph or nested table
    # wrapped in <w:ins> / <w:sdt> inside a cell must not be dropped.
    for child in _iter_effective_body_children(tc):
        tag = _local(child.tag)
        if tag == "p":
            paragraph_blocks = _convert_paragraph(
                child, ole_blobs=ole_blobs, chem_detection=chem_detection
            )
            for blk in paragraph_blocks:
                if isinstance(blk, MathBlock):
                    # Fold a display MathBlock into the cell as inline math:
                    # tables are visually constrained, display math doesn't
                    # fit. Deferred like every other inline OMML — emit a
                    # source-tagged island (carrying the block's own source
                    # dialect) for the frontend's math pass to convert.
                    parts.append(inline_math.wrap(blk.source, blk.text or ""))
                else:
                    parts.append(blk.text or "")
        elif tag == "tbl":
            # Nested table: the braille cell renderer wants flat text, so
            # fold the inner grid's cells into this cell (each inner cell
            # is one newline-separated chunk) instead of dropping them.
            nested = _convert_table(
                child, ole_blobs=ole_blobs, chem_detection=chem_detection
            )
            for row in nested.rows:
                for inner_cell in row.cells:
                    if inner_cell.text:
                        parts.append(inner_cell.text)
    return TableCell(text="\n".join(p for p in parts if p))
