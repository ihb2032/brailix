def _convert_paragraph(
    p: Element,
    *,
    ole_blobs: dict[str, bytes] | None = None,
    chem_detection: bool = False,
) -> list[Block]:
    """Convert a single ``<w:p>`` into one or more IR blocks.

    A paragraph that is **only** display math becomes a standalone
    :class:`MathBlock`. A paragraph mixing text + inline math becomes a
    :class:`Paragraph` (or :class:`Heading` / :class:`ListItem` based
    on style hints). Display-math children inside an otherwise-textual
    paragraph are extracted and emitted between paragraph slices so
    the math still renders as display, not inline.
    """
    if ole_blobs is None:
        ole_blobs = {}
    style = _paragraph_style(p)
    list_info = _paragraph_list_info(p, style=style)
    align = _paragraph_alignment(p)

    # First pass: split the paragraph at display-math boundaries.
    # Word allows ``m:oMathPara`` nested inside ``w:p``; treat each
    # one as a block boundary so display math gets its own block.
    text_parts: list[str] = []
    blocks_out: list[Block] = []

    def flush_text():
        nonlocal text_parts
        if text_parts:
            text = "".join(text_parts).strip()
            if text:
                blocks_out.append(
                    _wrap_text_block(
                        text, style=style, list_info=list_info, align=align
                    )
                )
            text_parts = []

    for run_text, math_block in _walk_paragraph_content(
        p, ole_blobs=ole_blobs, chem_detection=chem_detection
    ):
        if math_block is not None:
            flush_text()
            blocks_out.append(math_block)
        else:
            text_parts.append(run_text)
    flush_text()

    # Empty paragraph: keep an empty Paragraph so the layout reflects
    # the blank line — users sometimes use these as visual spacing.
    if not blocks_out and not style and list_info is None:
        return [Paragraph(text="")]

    return blocks_out


def _wrap_text_block(
    text: str,
    *,
    style: str | None,
    list_info: tuple[int, bool] | None,
    align: str | None = None,
) -> Block:
    """Pick the right block class for a paragraph slice.

    Order of precedence: list info wins over heading style (Word lets
    a "Heading 1" paragraph be a list item — that's still a list item
    in our IR); explicit heading style wins over plain paragraph.

    The list item carries an internal ``__ordered__`` marker via
    :attr:`Block.id` so :func:`_iter_body_blocks` knows what flavour
    of list to wrap it in. The id is cleared before yielding.

    ``align`` (the paragraph's ``w:jc``, already normalised to
    ``"center"`` / ``"right"`` / ``None``) is stamped on whichever block
    kind we produce — recording the source's intent regardless of kind;
    the backend / layout then decide which kinds to honour.
    """
    if list_info is not None:
        _level, ordered = list_info
        item = ListItem(text=text, align=align)
        if ordered:
            item.id = "__ordered__"
        return item
    if style is not None:
        m = _HEADING_STYLE_RE.match(style)
        if m:
            level = int(m.group(1))
            return Heading(level=level, text=text, align=align)
    return Paragraph(text=text, align=align)


def _walk_paragraph_content(
    p: Element,
    *,
    ole_blobs: dict[str, bytes] | None = None,
    chem_detection: bool = False,
):
    """Yield ``(text_piece, math_block | None)`` for each content unit.

    ``text_piece`` is the contribution to surrounding paragraph text
    (empty string when the unit is a standalone display block).
    ``math_block`` is non-None only for display equations
    (``m:oMathPara`` children); inline ``m:oMath`` produces a text piece
    carrying a deferred source-tagged inline-math island
    (:mod:`brailix.core.inline_math`) the frontend converts later.

    ``<w:object>`` elements containing a MathType / Equation 3.0 OLE
    object are extracted from ``ole_blobs`` and — because MTEF is binary,
    which the text IR can't carry — decoded through the MTEF adapter here
    into an eager ``$<math>...</math>$`` island.

    Word EQ fields (``<w:fldChar begin>`` + ``<w:instrText>eq ...</w:instrText>``
    + ``<w:fldChar end>``) span multiple runs; an :class:`_FieldState`
    instance threads the in-flight ``instrText`` buffer across run
    boundaries so the complete instruction is captured (and likewise
    deferred, as an ``eq_field`` island).

    Runs carrying ``<w:vertAlign>`` (the super/subscript a user sets via
    Ctrl+Shift+= / Ctrl+=) are not text that can be flattened away: a
    coalescing pass turns each maximal cluster of script-bearing runs into
    a linearised ``script_cluster`` island. Building the MathML and judging
    whether the cluster is chemistry happen in the frontend adapter;
    ``chem_detection`` only selects the chem-enabled source name — see
    :func:`_coalesce_script_clusters`.
    """
    if ole_blobs is None:
        ole_blobs = {}
    tokens = _iter_paragraph_tokens(p, ole_blobs)
    yield from _coalesce_script_clusters(tokens, chem_detection=chem_detection)


def _iter_paragraph_tokens(p: Element, ole_blobs: dict[str, bytes]):
    """Lower a ``<w:p>`` into a flat token stream for the coalescer.

    Each token is one of:

    * ``("text", str, vert)``  — run text, ``vert`` ∈ ``{None, "super",
      "sub"}`` from the run's ``<w:vertAlign>``;
    * ``("math", str)`` — an already-formed inline-math island, opaque to
      script coalescing: a deferred source-tagged island (inline OMML / EQ
      field) or an eager ``$<math>...$`` one (OLE MTEF);
    * ``("block", MathBlock)`` — a standalone display equation.

    This is the old body of :func:`_walk_paragraph_content`, re-expressed
    so the run-level vertical-alignment survives down to the coalescer
    instead of being flattened into bare text.
    """
    field_state = _FieldState()
    # Iterate top-level children only — ``.iter()`` would re-visit nested
    # elements once their parent has handled them. Each child is dispatched by
    # :func:`_emit_child_tokens`, which transparently recurses through
    # run-container wrappers (revision tracking, content controls, smart tags)
    # so their inline math / scripts survive instead of being scraped to text.
    for child in p:
        yield from _emit_child_tokens(child, ole_blobs, field_state)
    # A field still open at paragraph end never reached its `end`: its
    # instruction was never assembled and its visible-fallback text was held
    # back. Flush that text so an unclosed or cross-paragraph field doesn't
    # silently swallow the rest of the paragraph (the formula itself can't be
    # recovered without its `end`, but the visible text must not vanish).
    if field_state.in_field:
        leftover = field_state.take_unclosed_result()
        if leftover:
            yield ("text", leftover, None)


# Run-container wrappers Word places around ordinary inline content. They carry
# no content of their own — their children are runs / math / nested wrappers
# and must be walked the same way, never flattened to bare ``<w:t>`` text.
_TRANSPARENT_RUN_WRAPPERS = frozenset(
    {"ins", "del", "moveFrom", "moveTo", "smartTag", "customXml"}
)


def _emit_child_tokens(child: Element, ole_blobs, field_state):
    """Yield paragraph tokens for one direct child of a ``<w:p>`` (or of a
    transparent run-container wrapper).

    Factored out of :func:`_iter_paragraph_tokens` so revision-tracking
    (``<w:ins>`` / ``<w:del>`` / ``<w:moveFrom>`` / ``<w:moveTo>``),
    content-control (``<w:sdt>``), smart-tag and custom-XML wrappers can
    recurse through the *same* dispatch. Word emits ordinary runs — including
    inline ``<m:oMath>`` islands, ``<w:object>`` OLE math and run
    ``<w:vertAlign>`` scripts — inside these wrappers; the old text-only
    ``else`` scrape destroyed all of that (a tracked-changes insertion of a
    formula came out as bare literal text, an OLE equation vanished).
    """
    tag = _local(child.tag)
    if tag == "oMathPara":
        yield ("block", _math_block_from_omath_para(child))
    elif tag == "oMath":
        yield ("math", _inline_math_as_text(child))
    elif tag == "r":
        vert = _run_vert_align(child)
        for piece in _walk_run(child, ole_blobs, field_state):
            if _is_inline_math(piece):
                yield ("math", piece)
            else:
                yield ("text", piece, vert)
    elif tag == "hyperlink":
        # A hyperlink wraps ordinary runs / math — transparent to content.
        for hyper_child in child:
            yield from _emit_child_tokens(hyper_child, ole_blobs, field_state)
    elif tag == "object":
        text = _ole_object_to_inline_math(child, ole_blobs)
        if text:
            yield ("math", text)
    elif tag == "fldSimple":
        # Word's simple-field form: instruction in ``w:instr`` attribute,
        # cached result text as children. Skip the result (visual rendering).
        instr = _ns_attr(child, _W_PREFIX, "instr") or ""
        piece = _eq_field_to_inline_math(instr)
        if piece is not None:
            yield ("math", piece)
    elif tag == "AlternateContent":
        # Word wraps OLE objects in <mc:AlternateContent> when both a modern
        # (Choice) and legacy (Fallback) representation exist. Prefer Fallback
        # for OLE math — Choice is usually the picture preview we can't read.
        for piece, math in _walk_alternate_content(child, ole_blobs):
            if math is not None:
                yield ("block", math)
            elif piece:
                yield ("math", piece)
    elif tag in _TRANSPARENT_RUN_WRAPPERS:
        # Revision tracking / smart tag / custom XML: recurse into the inline
        # content the wrapper carries.
        for sub in child:
            yield from _emit_child_tokens(sub, ole_blobs, field_state)
    elif tag == "sdt":
        # Structured-document tag (content control): inline content lives under
        # <w:sdtContent>; <w:sdtPr> / <w:sdtEndPr> are properties, skip them.
        content = _first_local(child, "sdtContent")
        if content is not None:
            for sub in content:
                yield from _emit_child_tokens(sub, ole_blobs, field_state)
    elif tag == "pPr":
        # Paragraph properties — already consumed by style / list detection.
        return
    else:
        # Genuinely unknown wrapping element: descend transparently through the
        # SAME dispatch as the named run wrappers, so inline math / OLE / EQ
        # fields / vertAlign scripts under an unrecognised wrapper survive
        # instead of being flattened to bare <w:t> text (which also dropped the
        # vertical-alignment context). In well-formed OOXML <w:t> always lives
        # inside a <w:r>, which the 'r' branch above walks; property sub-trees
        # (rPr/pPr/...) recurse to nothing, so this stays safe.
        for sub in child:
            yield from _emit_child_tokens(sub, ole_blobs, field_state)


