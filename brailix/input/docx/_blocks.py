# Run-level vertical alignment → inline math (Ctrl+= / Ctrl+Shift+= scripts)
# ---------------------------------------------------------------------------

# Word's ``w:val`` for a raised / lowered run. ``baseline`` (and a missing
# element) is ordinary text.
_VERT_ALIGN = {"superscript": "super", "subscript": "sub"}

# Half-width operators that may glue a formula together inside a script
# cluster — the cluster alphabet, alongside ASCII alphanumerics. Operator
# canonicalisation and the cluster → MathML build (with chemistry detection)
# now live in the frontend ``script_cluster`` adapter; this layer only
# gathers the cluster and linearises it.
_CLUSTER_OPS = frozenset("+-()[]")


def _run_vert_align(r: Element) -> str | None:
    """Return ``"super"`` / ``"sub"`` for a run carrying ``<w:vertAlign>``.

    Word writes ``<w:rPr><w:vertAlign w:val="superscript|subscript"/>``
    when text is raised / lowered via the Ctrl+Shift+= / Ctrl+= shortcuts
    (or the Font dialog). ``"baseline"`` and the absence of the element
    both mean ordinary text → ``None``.
    """
    rpr = _first_local(r, "rPr")
    if rpr is None:
        return None
    va = _first_local(rpr, "vertAlign")
    if va is None:
        return None
    val = _ns_attr(va, _W_PREFIX, "val")
    return _VERT_ALIGN.get(val or "")


def _is_inline_math(piece: str) -> bool:
    """True for an already-formed inline-math island (so the coalescer
    treats it as an opaque unit, never script text).

    Covers both forms a paragraph token can carry: a deferred
    source-tagged island (inline OMML / EQ field, converted later by the
    frontend) and the eagerly-built ``$<math>...</math>$`` form still used
    by the MTEF / script-cluster paths.
    """
    return inline_math.is_tagged(piece) or (
        piece.startswith(_INLINE_MATH_OPEN) and piece.endswith("$")
    )


def _is_cluster_char(ch: str) -> bool:
    """A character that may join a sub/superscript cluster: ASCII letters /
    digits and the handful of half-width operators that hold a formula
    together. Whitespace, CJK, and prose punctuation are boundaries — they
    end a cluster so the surrounding prose stays prose."""
    return ch.isascii() and (ch.isalnum() or ch in _CLUSTER_OPS)


def _coalesce_script_clusters(tokens, *, chem_detection: bool = False):
    """Turn the token stream from :func:`_iter_paragraph_tokens` back into
    ``(text_piece, math_block)`` pairs, folding every cluster of
    script-bearing run text into one deferred ``script_cluster`` island.

    Consecutive ``text`` tokens accumulate; an inline-math island or a
    display block flushes them. Flushing renders the buffered text,
    replacing each maximal run of cluster characters that contains at
    least one script with inline math (:func:`_render_text_with_scripts`).
    Inline pieces are concatenated into a single text yield (matching the
    old per-paragraph join) with a guard space inserted between two
    abutting ``$...$`` islands so the segmenter's inline-math regex — which
    rejects ``$$`` — still sees each one.
    """
    inline: list[str] = []
    text_buf: list[tuple[str, str | None]] = []

    def append_inline(part: str) -> None:
        if part and inline and inline[-1].endswith("$") and part.startswith("$"):
            inline.append(" ")
        if part:
            inline.append(part)

    def flush_text() -> None:
        if text_buf:
            append_inline(
                _render_text_with_scripts(text_buf, chem_detection=chem_detection)
            )
            text_buf.clear()

    def take_inline() -> str:
        combined = "".join(inline)
        inline.clear()
        return combined

    for tok in tokens:
        kind = tok[0]
        if kind == "text":
            text_buf.append((tok[1], tok[2]))
        elif kind == "math":
            flush_text()
            append_inline(tok[1])
        elif kind == "block":
            flush_text()
            combined = take_inline()
            if combined:
                yield combined, None
            yield "", tok[1]
    flush_text()
    combined = take_inline()
    if combined:
        yield combined, None


def _render_text_with_scripts(
    buf: list[tuple[str, str | None]], *, chem_detection: bool
) -> str:
    """Render buffered ``(text, vert)`` tokens, replacing script clusters
    with inline math and leaving everything else as literal text.

    The tokens are expanded to ``(char, vert)`` — whitespace forced to the
    baseline so it always breaks a cluster — then scanned into maximal runs
    of cluster characters. A run holding at least one script char becomes
    inline math; plain runs and non-cluster characters pass through
    unchanged.
    """
    chars: list[tuple[str, str | None]] = []
    for text, vert in buf:
        for ch in text:
            chars.append((ch, None if ch.isspace() else vert))

    out: list[str] = []
    i = 0
    n = len(chars)
    while i < n:
        ch, vert = chars[i]
        if vert is None and not _is_cluster_char(ch):
            out.append(ch)
            i += 1
            continue
        j = i
        has_script = False
        while j < n:
            cj, vj = chars[j]
            if vj is None and not _is_cluster_char(cj):
                break
            if vj is not None:
                has_script = True
            j += 1
        run = chars[i:j]
        if has_script:
            out.append(_cluster_to_inline_math(run, chem_detection=chem_detection))
        else:
            out.append("".join(c for c, _ in run))
        i = j
    return "".join(out)


def _cluster_to_inline_math(
    run: list[tuple[str, str | None]], *, chem_detection: bool
) -> str:
    """Encode one Word script cluster as a deferred inline-math island.

    The cluster — formatted-text sub/superscripts like ``x²`` / ``H₂O`` — is
    *linearised* to a compact ``base ^{..} _{..}`` source string and wrapped
    as a source-tagged island (:mod:`brailix.core.inline_math`). Building the
    MathML, and judging whether the cluster is chemistry, is the frontend's
    job: the ``script_cluster`` adapter does it later (ARCHITECTURE §1). The
    ``chem_detection`` *config* selects the source name; the chemical
    *judgment* itself runs in the adapter, not here.
    """
    source = "script_cluster_chem" if chem_detection else "script_cluster"
    return inline_math.wrap(source, _linearise_cluster(run))


def _linearise_cluster(run: list[tuple[str, str | None]]) -> str:
    """Linearise a script cluster to a ``base ^{..} _{..}`` source string.

    Baseline characters pass through; a maximal superscript run becomes
    ``^{...}`` and a subscript run ``_{...}``. The cluster alphabet (ASCII
    alphanumerics plus ``+-()[]``) never contains ``^`` / ``_`` / ``{`` /
    ``}``, so no escaping is needed and the frontend ``script_cluster``
    adapter parses it back unambiguously.
    """
    parts: list[str] = []
    i, n = 0, len(run)
    while i < n:
        ch, vert = run[i]
        if vert is None:
            parts.append(ch)
            i += 1
            continue
        j = i
        while j < n and run[j][1] == vert:
            j += 1
        chunk = "".join(c for c, _ in run[i:j])
        parts.append(("^{" if vert == "super" else "_{") + chunk + "}")
        i = j
    return "".join(parts)


# ---------------------------------------------------------------------------
# Word field state machine (EQ fields span multiple runs)
# ---------------------------------------------------------------------------


class _FieldState:
    """In-flight state for ``<w:fldChar>`` / ``<w:instrText>`` parsing.

    A Word field looks like::

        <w:r><w:fldChar w:fldCharType="begin"/></w:r>
        <w:r><w:instrText>eq \\f(1,2)</w:instrText></w:r>
        <w:r><w:fldChar w:fldCharType="separate"/></w:r>   ← optional
        <w:r><w:t>cached visible result</w:t></w:r>        ← skipped
        <w:r><w:fldChar w:fldCharType="end"/></w:r>

    The instruction text accumulates between ``begin`` and
    ``separate`` (or ``end`` if no separate). After ``separate`` we're
    in the cached-result section, which we discard because the
    authoritative content is the instruction itself. Nested fields are
    supported via ``depth``; only depth-1 EQ instructions are
    converted (nested ones are part of the outer instruction text).
    """

    __slots__ = ("depth", "after_separate", "instr_buf", "result_buf")

    def __init__(self) -> None:
        self.depth: int = 0
        self.after_separate: bool = False
        self.instr_buf: list[str] = []
        # Visible-fallback text buffered while in the cached-result section.
        # Discarded on a normal `end` (so it never doubles converted math),
        # but flushed by the paragraph walker if the field is left unclosed.
        self.result_buf: list[str] = []

    @property
    def in_field(self) -> bool:
        return self.depth > 0

    @property
    def collecting_instr(self) -> bool:
        """True iff we're inside a field but before its ``separate`` marker."""
        return self.depth > 0 and not self.after_separate

    @property
    def in_cached_result(self) -> bool:
        """True iff inside a field's cached-result (visible-fallback) section
        — after ``separate``, before ``end``."""
        return self.depth > 0 and self.after_separate

    def begin(self) -> None:
        if self.depth == 0:
            self.instr_buf = []
            self.result_buf = []
            self.after_separate = False
        self.depth += 1

    def separate(self) -> None:
        if self.depth > 0:
            self.after_separate = True

    def end(self) -> str | None:
        """Close one field level. Returns the assembled instruction
        text if a top-level field just closed, else None."""
        if self.depth == 0:
            return None
        self.depth -= 1
        if self.depth == 0:
            instr = "".join(self.instr_buf)
            self.instr_buf = []
            # A normally-closed field's visible fallback is discarded — the
            # instruction (e.g. converted EQ math) is the authoritative form.
            self.result_buf = []
            self.after_separate = False
            return instr
        return None

    def add_instr(self, text: str) -> None:
        if self.collecting_instr:
            self.instr_buf.append(text)

    def add_result(self, text: str) -> None:
        if self.in_cached_result:
            self.result_buf.append(text)

    def take_unclosed_result(self) -> str:
        """Drain the buffered visible-fallback text of a field left open at
        paragraph end (unclosed, or whose ``end`` lands in a later
        paragraph), so it is recovered as text instead of silently eaten."""
        text = "".join(self.result_buf)
        self.result_buf = []
        return text


def _eq_field_to_inline_math(instr: str) -> str | None:
    """Encode an EQ field ``instr`` as a deferred ``eq_field`` island.

    Returns ``None`` for non-EQ fields (HYPERLINK, PAGE, TOC, …) so the
    caller can skip them silently. The "is this an EQ field?" gate stays
    here (input-side), but the EQ→MathML conversion is *deferred*: the
    raw instruction is wrapped as a source-tagged island
    (:func:`brailix.core.inline_math.wrap`) for the frontend's math pass
    to convert, so this module takes no math-frontend dependency. The EQ
    adapter wraps its own parse failures in ``<merror>``, so a malformed
    equation still surfaces as inline math (with a MATH_ERROR warning).
    """
    text = instr.strip()
    if not text:
        return None
    # Field code is the first whitespace-delimited word.
    head = text.split(None, 1)[0].lower()
    if head != "eq":
        return None
    return inline_math.wrap("eq_field", text)


def _walk_run(
    r: Element,
    ole_blobs: dict[str, bytes],
    field_state: _FieldState | None = None,
):
    """Yield text pieces inside a ``<w:r>``, including any OLE objects.

    Word commonly nests ``<w:object>`` inside a run when the equation
    is inline with text. Returning a list keeps the caller's
    ``(text, math)`` contract intact — every yielded piece is a string
    that gets appended to the surrounding paragraph text.

    ``field_state`` (when provided) tracks ``w:fldChar`` boundaries
    across runs so EQ fields whose instruction spans multiple runs
    still parse correctly. Text inside the field's cached-result
    section (between ``separate`` and ``end``) is silently dropped so
    Word's visible-text fallback doesn't end up duplicated alongside
    the converted math.
    """
    parts: list[str] = []
    for child in r:
        tag = _local(child.tag)
        if tag == "fldChar":
            if field_state is None:
                continue
            ftype = _ns_attr(child, _W_PREFIX, "fldCharType")
            if ftype == "begin":
                field_state.begin()
            elif ftype == "separate":
                field_state.separate()
            elif ftype == "end":
                instr = field_state.end()
                if instr is not None:
                    piece = _eq_field_to_inline_math(instr)
                    if piece is not None:
                        parts.append(piece)
            continue
        if tag == "instrText":
            if field_state is not None and field_state.collecting_instr:
                field_state.add_instr(child.text or "")
            continue
        if field_state is not None and field_state.in_cached_result:
            # Inside the field's cached-result (visible-fallback) section.
            # Buffer it rather than dropping outright: a normally-closing
            # field discards this on `end` (so it never doubles the converted
            # math), but an unclosed field — no `end`, or an `end` in a later
            # paragraph — flushes it back at paragraph end instead of
            # silently eating the rest of the paragraph.
            if tag == "t":
                field_state.add_result(child.text or "")
            elif tag == "br":
                field_state.add_result("\n")
            elif tag == "tab":
                field_state.add_result(" ")
            continue
        if tag == "t":
            parts.append(child.text or "")
        elif tag == "br":
            parts.append("\n")
        elif tag == "tab":
            parts.append(" ")
        elif tag == "object":
            text = _ole_object_to_inline_math(child, ole_blobs)
            if text:
                parts.append(text)
        elif tag == "AlternateContent":
            for piece, math in _walk_alternate_content(child, ole_blobs):
                if math is not None:
                    # A display equation (oMathPara) in a run-nested
                    # AlternateContent comes back as ("", MathBlock). _walk_run
                    # yields string pieces, so fold the display block in as an
                    # inline-math island (mirrors _convert_table_cell) rather
                    # than discard the _math slot and silently lose the formula.
                    parts.append(inline_math.wrap(math.source, math.text or ""))
                elif piece:
                    parts.append(piece)
    yield from parts


def _walk_alternate_content(
    alt: Element, ole_blobs: dict[str, bytes]
):
    """Walk an ``<mc:AlternateContent>`` for math content.

    Strategy: try Fallback first — that's where Word stores the legacy
    ``<w:object>`` form when also offering a modern drawing in Choice.
    Choice is descended only if Fallback yields nothing.
    """
    fallback = _first_local(alt, "Fallback")
    if fallback is not None:
        pieces = list(_walk_alt_subtree(fallback, ole_blobs))
        # Short-circuit Choice only when Fallback yielded *real* content. An
        # empty placeholder run yields ("", None), which used to set produced=
        # True and suppress Choice — silently dropping a formula that lived
        # only in Choice. Consumers filter empty pieces, so yielding them is
        # harmless; the decision must hinge on whether any was non-empty.
        if any(piece or math is not None for piece, math in pieces):
            yield from pieces
            return
    for choice in alt:
        if _local(choice.tag) == "Choice":
            yield from _walk_alt_subtree(choice, ole_blobs)


def _walk_alt_subtree(
    node: Element,
    ole_blobs: dict[str, bytes],
    field_state: _FieldState | None = None,
):
    """Descend into an AlternateContent branch, surfacing math content.

    The branch can be any depth — Word sometimes nests another
    ``<w:r>`` or ``<w:p>`` here. We dispatch through the same machinery
    used by paragraph walking, just without the paragraph-level
    splitting (the caller's run/paragraph context already has that).

    Note: this re-implements the object / oMath / oMathPara / r tag
    dispatch of :func:`_iter_paragraph_tokens` (different return contract —
    ``(piece, math)`` here vs. tokens there). A new inline-math source added
    to that walker must be mirrored here, or AlternateContent branches will
    silently drop it.

    A single ``_FieldState`` is threaded through the whole branch (created at
    the top-level call, passed down through recursion) so a cross-run EQ field
    — ``fldChar begin`` / ``instrText`` / ``end`` spread over separate runs —
    assembles instead of being dropped (``_walk_run`` skips ``fldChar`` when no
    state is passed). Unclosed text is flushed at the branch end, matching the
    paragraph walker.
    """
    if field_state is None:
        field_state = _FieldState()
        own_state = True
    else:
        own_state = False
    for child in node:
        tag = _local(child.tag)
        if tag == "object":
            text = _ole_object_to_inline_math(child, ole_blobs)
            if text:
                yield text, None
        elif tag == "oMath":
            yield _inline_math_as_text(child), None
        elif tag == "oMathPara":
            yield "", _math_block_from_omath_para(child)
        elif tag == "r":
            for piece in _walk_run(child, ole_blobs, field_state):
                yield piece, None
        else:
            # Recurse — the OLE might be wrapped in another shape /
            # drawing element we don't directly handle.
            yield from _walk_alt_subtree(child, ole_blobs, field_state)
    if own_state and field_state.in_field:
        leftover = field_state.take_unclosed_result()
        if leftover:
            yield leftover, None


def _math_block_from_omath_para(node: Element) -> MathBlock:
    """Wrap a standalone display equation in a :class:`MathBlock`.

    The block carries the raw OMML XML; the math frontend's OMML
    adapter does the conversion at Pipeline time. Keeping the
    conversion lazy here (rather than pre-MathML'ing) keeps the input
    layer's job simple — "extract structure", not "translate dialects".
    """
    omml_xml = _serialize(node)
    return MathBlock(source="omml", text=omml_xml)


